# Join Function
# used for joining all element of list with an join elem in single string

lis = ['Ved' , "Ajay" , "Anchal" , "sudhanshu" , "Abhishek"]

# join operaton
string = " and ".join(lis)

print(string)
# Output : Ved and Ajay and Anchal and sudhanshu and Abhishek

# ---------------------------------------------------------- #
string = " , ".join(lis)

print(string)

# Output : Ved , Ajay , Anchal , sudhanshu , Abhishek
